package com.abayhq.browniesnfriends.settergetter;

public class dataNotaTransaksi {
    String no_nota;
    String nama;
    String tanggal_pengambilan;
    String jam;
    String grand_total;
    String status_bayar;
    String kurang_bayar;
    String status_batal;

    public String getNo_nota() {
        return no_nota;
    }

    public void setNo_nota(String no_nota) {
        this.no_nota = no_nota;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTanggal_pengambilan() {
        return tanggal_pengambilan;
    }

    public void setTanggal_pengambilan(String tanggal_pengambilan) {
        this.tanggal_pengambilan = tanggal_pengambilan;
    }

    public String getJam() {
        return jam;
    }

    public void setJam(String jam) {
        this.jam = jam;
    }

    public String getGrand_total() {
        return grand_total;
    }

    public void setGrand_total(String grand_total) {
        this.grand_total = grand_total;
    }

    public String getStatus_bayar() {
        return status_bayar;
    }

    public void setStatus_bayar(String status_bayar) {
        this.status_bayar = status_bayar;
    }

    public String getKurang_bayar() {
        return kurang_bayar;
    }

    public void setKurang_bayar(String kurang_bayar) {
        this.kurang_bayar = kurang_bayar;
    }

    public String getStatus_batal() {
        return status_batal;
    }

    public void setStatus_batal(String status_batal) {
        this.status_batal = status_batal;
    }
}
